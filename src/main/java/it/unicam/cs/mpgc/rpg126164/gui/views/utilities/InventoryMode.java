package it.unicam.cs.mpgc.rpg126164.gui.views.utilities;

/**
 * This enum works as an inventory selector, in order to show only weapon or potions for the fight items
 * view of the game
 */
public enum InventoryMode {

    /**
     * Must show the weapons and equipments
     */
    WEAPON,

    /**
     * Must show all the potions and consumables
     */
    POTION
}
